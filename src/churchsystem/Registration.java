    private void txtsearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtsearchKeyReleased
            jPasswordField1.setEditable(false);
            jPasswordField2.setEditable(false);
            jButton2.setEnabled(true);
            jButton4.setEnabled(true);
      String st=txtsearch.getText().trim();
      if(st.equals("")){
           clear();
       }else{
String sql="Select * from registration where username like '"+st+"%' OR Firstname like '"+st+"%' OR Surname like '"+st+"%' OR contact like '"+st+"%' ";

    try {
        pst=conn.prepareStatement(sql);
        rs=pst.executeQuery();
        if(rs.next()){
             String id = rs.getString("id");
        jLabel13.setText(id);
          jLabel13.show();
          
          String fn = rs.getString("Firstname");
        txtfn.setText(fn);
        
        String user = rs.getString("username");
        txtUser.setText(user);
        
        String sn = rs.getString("Surname");
        txtSn.setText(sn);
        
       Date dob = rs.getDate("Date_O_B");
         jDateChooser1.setDate(dob);
        
        String contact = rs.getString("contact");
         txtContact.setText(contact);
         
         String pass = rs.getString("password");
         jPasswordField1.setText(pass);
         
          String held = rs.getString("status");
        jComboBox1.setSelectedItem(held);
        
                     

        } else{
          JOptionPane.showMessageDialog(null, "No Record Found");
          clear();
        } 
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, ex.getMessage());
        
    }
    } 
    }//GEN-LAST:event_txtsearchKeyReleased

