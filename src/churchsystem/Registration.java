    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
               int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to add record?", "Add Record", JOptionPane.YES_NO_OPTION);
            if (p == 0) {
                if(txtfn.getText().equals("") || txtSn.getText().equals("")
                    || jDateChooser1.getDate()==null || txtContact.getText().equals("")
                     || txtUser.getText().equals("")|| jPasswordField1.getText().equals("")
                    || jComboBox1.getSelectedIndex()==0   ){
                    JOptionPane.showMessageDialog(null, "Complete  Information", "Missing Information", 2);
                }
                else{

                try {
                    String sql = "insert into registration "
                            + "(Firstname,Surname,Date_O_B,contact,username,password,Status) values (?,?,?,?,?,?,?) ";

                    pst = conn.prepareStatement(sql);
                    
                    pst.setString(1, txtfn.getText().trim());
                    pst.setString(2, txtSn.getText().trim());
                    
                    SimpleDateFormat dateFormat=new SimpleDateFormat("yyy-MM-dd");
                    String dob=dateFormat.format(jDateChooser1.getDate());
                    pst.setString(3,dob);
                    
                    pst.setString(4, txtContact.getText().trim());
                    pst.setString(5, txtUser.getText().trim().toLowerCase());
                    pst.setString(6, jPasswordField1.getText().trim());
                    pst.setString(7, jComboBox1.getSelectedItem().toString().trim());
                    
                    pst.execute();
                    clear();
                    JOptionPane.showMessageDialog(null, "Data is saved successfully");
                   
                } catch (HeadlessException | SQLException e) {
                    JOptionPane.showMessageDialog(null, e);
  
              }
                        }
                
                }
                      
            
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

