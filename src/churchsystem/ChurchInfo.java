boolean checkInputs(){
       if(txtSchoolName.getText()==null || txtAddress.getText()==null || txtTelephone1.getText()==null 
               || txtTelephone2.getText()==null ){
           return false;
       }else{
           try{
               Integer.parseInt(txtTelephone1.getText());
               Integer.parseInt(txtTelephone2.getText());
               return true;
           }catch(NumberFormatException e ){
             JOptionPane.showMessageDialog(null,e.getMessage());
             return false;
           }
       
       }
    
}
 void schoolInfo(){
      try{
        
        
        String sql ="select * from school_info";
        
        ps=con.prepareStatement(sql);
        rs=ps.executeQuery();
        if(rs.next()){
            
                 String id = rs.getString("id");
                jLabelId.setText(id);
            
                 String name = rs.getString("Name");
                txtSchoolName.setText(name);

                 String adress = rs.getString("Address");
                txtAddress.setText(adress);
                
                String num1 = rs.getString("Number1");
                txtTelephone1.setText('0'+num1);

                String num2 = rs.getString("Number2");
                txtTelephone2.setText('0'+num2);

                 byte[]pic=rs.getBytes("logo");
                
                if(pic == null){
                    
                }else{
                ImageIcon image=new ImageIcon(pic);
                Image im= image.getImage();
                Image myimg=im.getScaledInstance(lblPicture.getWidth(), lblPicture.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon newImage=new ImageIcon(myimg);
                lblPicture.setIcon(newImage);
                }
        
        }

    }
    catch(SQLException e){
    JOptionPane.showMessageDialog(null, e);
    }
 
 }
