    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
 int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to Save This  SChool Information?","Save Record",JOptionPane.YES_NO_OPTION);
if(p==0){
        if(checkInputs() && imagePath !=null){
                try{

ps=con.prepareStatement("INSERT INTO school_info (Name,Address,Number1,Number2,logo)"
                    + " values(?,?,?,?,?)");

                ps.setString(1, txtSchoolName.getText().trim());
                ps.setString(2, txtAddress.getText().trim());
                ps.setString(3, txtTelephone1.getText().trim());
                ps.setString(4, txtTelephone2.getText().trim());
                InputStream img;
                img = new FileInputStream(new File(imagePath));
                ps.setBlob(5,img);
                
                ps.execute();
                schoolInfo();
               
                JOptionPane.showMessageDialog(null,"Data is saved successfully");
            }
            catch(HeadlessException | FileNotFoundException | SQLException e){
                JOptionPane.showMessageDialog(null,e.getMessage());

            }
            finally {
                try{
                   
                    con.close();
                }
                catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "One or more field is empty");
        
        }

}
    }//GEN-LAST:event_jButton8ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
                     ///Selecting images
        try{
            JFileChooser file = new JFileChooser();
            file.setCurrentDirectory(new File(System.getProperty("user.home")));

            FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images","jpg","png");

            file.addChoosableFileFilter(filter);
            int result =file.showSaveDialog(null);
            if(result==JFileChooser.APPROVE_OPTION){
                File selectedFile=file.getSelectedFile();
                String path=selectedFile.getAbsolutePath();
                lblPicture.setIcon(ResizeImage(path,null));
                imagePath=path;
            }
            else{
                JOptionPane.showMessageDialog(null, "No file");
            }
        }
        catch(HeadlessException ex){
            JOptionPane.showMessageDialog(null, "Error!");

        }
    }//GEN-LAST:event_jButton7ActionPerformed

